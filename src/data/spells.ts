// Функция для получения данных заклинания по названию
export const getSpellDetails = (name: string) => {
  return spellsDict[name];
};

// Функция для получения списка всех доступных заклинаний
export const getAllSpellNames = (): string[] => {
  return Object.keys(spellsDict);
};

// Функция для получения заклинаний определенного уровня
export const getSpellsByLevel = (level: number): string[] => {
  return Object.entries(spellsDict)
    .filter(([_, spell]) => spell.level === level)
    .map(([name, _]) => name);
};

// Функция для получения заклинаний определенного класса
export const getSpellsByClass = (className: string): string[] => {
  return Object.entries(spellsDict)
    .filter(([_, spell]) => spell.classes?.includes(className))
    .map(([name, _]) => name);
};

// Функция для получения заклинаний определенной школы
export const getSpellsBySchool = (school: string): string[] => {
  return Object.entries(spellsDict)
    .filter(([_, spell]) => spell.school === school)
    .map(([name, _]) => name);
};

// Дополнительная функция для поиска заклинаний
export const searchSpells = (query: string): string[] => {
  const lowerQuery = query.toLowerCase();
  
  return Object.entries(spellsDict)
    .filter(([name, spell]) => 
      name.toLowerCase().includes(lowerQuery) || 
      spell.description.toLowerCase().includes(lowerQuery) ||
      (spell.school && spell.school.toLowerCase().includes(lowerQuery))
    )
    .map(([name, _]) => name);
};
