// Пример функции для получения профессий определенной предыстории
export const getBackgroundProficiencies = (backgroundName: string): string[] => {
  const background = backgrounds.find(bg => bg.value === backgroundName);
  if (!background) return [];
  
  // Собираем все профессии
  const proficiencies: string[] = [];
  const bgProficiencies = background.proficiencies;
  
  // Добавляем навыки
  if (bgProficiencies.skills) {
    proficiencies.push(...bgProficiencies.skills.map(skill => `Навык: ${skill}`));
  }
  
  // Добавляем инструменты
  if (bgProficiencies.tools) {
    proficiencies.push(...bgProficiencies.tools.map(tool => `Инструмент: ${tool}`));
  }
  
  // Добавляем языки
  if (bgProficiencies.languages && bgProficiencies.languages > 0) {
    proficiencies.push(`Языки: ${bgProficiencies.languages} на выбор`);
  }
  
  return proficiencies;
};
